// Custom command for login
Cypress.Commands.add('login', (email, password) => {
  cy.visit('/login');
  cy.get('input[name="email"]').type(email);
  cy.get('input[name="password"]').type(password);
  cy.get('button[type="submit"]').click();
  cy.url().should('include', '/dashboard');
});

// Custom command for checking content table
Cypress.Commands.add('checkContentTable', () => {
  cy.get('table').should('exist');
  cy.get('table thead').contains('Content');
  cy.get('table thead').contains('User');
  cy.get('table thead').contains('Type');
  cy.get('table thead').contains('Status');
  cy.get('table thead').contains('Date');
  cy.get('table thead').contains('Actions');
  cy.get('table tbody tr').should('have.length.at.least', 1);
});